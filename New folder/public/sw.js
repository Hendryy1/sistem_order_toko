// Service Worker - menerima push notification walau Web App sudah ditutup.
// File ini HARUS ditaruh di folder `public/` project Vite Anda (bukan di
// dalam src/), supaya ter-deploy persis di /sw.js (bukan di-bundle seperti
// file JS biasa).

self.addEventListener("push", (event) => {
  let data = {};
  try {
    data = event.data ? event.data.json() : {};
  } catch (e) {
    data = { title: "Notifikasi", body: event.data ? event.data.text() : "" };
  }

  const title = data.title || "Notifikasi";
  const options = {
    body: data.body || "",
    icon: "/icon-192.png",
    badge: "/icon-192.png",
    data: { url: data.url || "/" },
  };

  event.waitUntil(self.registration.showNotification(title, options));
});

self.addEventListener("notificationclick", (event) => {
  event.notification.close();
  const url = event.notification.data?.url || "/";
  event.waitUntil(
    clients.matchAll({ type: "window", includeUncontrolled: true }).then((windowClients) => {
      for (const client of windowClients) {
        if (client.url.includes(self.location.origin) && "focus" in client) {
          return client.focus();
        }
      }
      if (clients.openWindow) return clients.openWindow(url);
    })
  );
});
